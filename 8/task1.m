clear;
clc;
f1=50;
f2=80;
f3=100;
f=max([f1, f2, f3]);
fs=3*f;
Ts=1/fs;
t=0:Ts:1;
x=1.2*sin(2*pi*f1*t)+0.8*sin(2*pi*f2*t)+0.6*sin(2*pi*f3*t);
noise=rand(1, length(t));
xn=x+noise;
subplot(221); plot(t, x); grid on; title('Original Signal');
subplot(222); plot(t, xn); grid on; title('Noisy Signal');

X=fft(x, 1024);
XN=fft(xn, 1024);
fp=fs*(0:511)/1024;
subplot(223); plot(fp, abs(X(1:512))); grid on; title('FFT of the Original Signal');
subplot(224); plot(fp, abs(XN(1:512))); grid on; title('FFT of the Noisy Signal');